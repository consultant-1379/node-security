package com.ericsson.oss.services.security.nscs.ldap.service;

import static java.lang.annotation.ElementType.TYPE;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

import java.lang.annotation.Documented;
import java.lang.annotation.Retention;
import java.lang.annotation.Target;

import javax.inject.Qualifier;

/**
 * Annotation to qualify the correct MOLdapService implementation according to the node platform.
 */
@Qualifier
@Documented
@Retention(RUNTIME)
@Target(TYPE)
public @interface MOLdapServiceType {
    String moLdapServiceType();

}
